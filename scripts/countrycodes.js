// Function to get the flag emoji based on country code
function getCountryFlagEmoji(countryCode) {
    const OFFSET = 127397;
    const codePoints = countryCode
        .toUpperCase()
        .split('')
        .map(char => char.charCodeAt() + OFFSET);
    return String.fromCodePoint(...codePoints);
}